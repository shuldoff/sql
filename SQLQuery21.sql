CREATE VIEW МастерИегоРабота AS
SELECT Учетная_карта.[дата_ввода_в эксплуатацию] AS ДАТА_наПИСАНИЯ,
Справочник_ответственных_лиц.ФИО AS Мастер,
справочник_нематериальных_активов.наименование AS тип
FROM Учетная_карта INNER JOIN справочник_нематериальных_активов ON Учетная_карта.код_вида=справочник_нематериальных_активов.код_вида
INNER JOIN Справочник_ответственных_лиц ON Учетная_карта.код_ответственного_лица=справочник_нематериальных_активов.наименование
